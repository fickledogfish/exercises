package main

import (
	"fmt"
	"io/ioutil"
	"strconv"
	"strings"
)

func readNum(filename string) string {
	num, err := ioutil.ReadFile(filename)

	if err != nil {
		return ""
	}

	return strings.Replace(string(num), "\n", "", -1)
}

func convertNum(num string) []uint8 {
	convertedNum := make([]uint8, len(num))

	for i, r := range num {
		n, _ := strconv.ParseUint(string(r), 10, 8)
		convertedNum[i] = uint8(n)
	}

	return convertedNum
}

func sliceProd(s []uint8) uint64 {
	prod := uint64(1)

	for _, i := range s {
		prod *= uint64(i)
	}

	return prod
}

func adjecentProd(num []uint8, numDigits uint16) uint64 {
	prod := uint64(1)

	for i := uint16(0); i < uint16(len(num))-numDigits; i++ {
		s := num[i : i+numDigits]
		p := sliceProd(s)

		if p > prod {
			prod = p
		}
	}

	return prod
}

